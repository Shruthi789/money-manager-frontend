import * as React from 'react';
import PropTypes from 'prop-types';
import Tabs from '@mui/material/Tabs';
import Tab from '@mui/material/Tab';
import Typography from '@mui/material/Typography';
import Box from '@mui/material/Box';
import {context} from './Home.js';
import {FormComponent} from './Form.js';

function TabPanel(props) {
  const { children, value, index, ...other } = props;

  return (
    <div
      role="tabpanel"
      hidden={value !== index}
      id={`simple-tabpanel-${index}`}
      aria-labelledby={`simple-tab-${index}`}
      {...other}
    >
      {value === index && (
        <Box sx={{ p: 3 }}>
         {children}
        </Box>
      )}
    </div>
  );
}

TabPanel.propTypes = {
  children: PropTypes.node,
  index: PropTypes.number.isRequired,
  value: PropTypes.number.isRequired,
};

function a11yProps(index) {
  return {
    id: `simple-tab-${index}`,
    'aria-controls': `simple-tabpanel-${index}`,
  };
}

export default function BasicTabs() {
  const [value, setValue] = React.useState(0);

  const handleChange = (event, newValue) => {
    setValue(newValue);
  };
  const labels=["Income","Expenditure"];
  const {submitHandler,categoriesIncome,initialValues}=React.useContext(context);
  return (
    <Box sx={{ width: '100%' }}>
      <Box sx={{ borderBottom: 1, borderColor: 'divider' }}>
        <Tabs value={value} onChange={handleChange} aria-label="Income-expenditure-tabs">
          {labels.map((label,index)=><Tab label={label} key={index} {...a11yProps(index)} />)}
        </Tabs>
      </Box>
      {labels.map((label,index)=> <TabPanel value={value} key={index} index={index}>
        <FormComponent submitHandler={submitHandler} categoriesIncome={categoriesIncome} initialValues={initialValues}/>
      </TabPanel>)}
    </Box>
  );
}